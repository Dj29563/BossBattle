function playerHealAnimation(n) {
    var increment = 1;
    var healedAmount = 0;
    var intervalTime = 50;
    var interval = setInterval(function() {
        if (healedAmount >= n) {
            clearInterval(interval);
            return;
        }

        if (playerhealth + increment >= 100) {
            playerhealth = 100;
            updateplayerhealth();
            clearInterval(interval);
        } else {
            playerhealth += increment;
            updateplayerhealth();
            healedAmount += increment;
        }

        if (playerhealth === 100) {
            clearInterval(interval);
        }
    }, intervalTime);
}

