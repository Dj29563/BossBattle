function toggleGameContainer() {
    const gameContainer = document.getElementById('game-container');
    const moveLeftButton = document.getElementById('moveleft');
    const moveRightButton = document.getElementById('moveright');
    const questionBox = document.getElementById('question-box');

    const isVisible = gameContainer.classList.contains('visible');

    if (isVisible) {
        let opacity = 1;
        let height = 30;
        const interval = 10;
        const steps = 30;
        moveLeftButton.style.display = 'none';
        moveRightButton.style.display = 'none';
        questionBox.style.display = 'none';
        const decreaseHeight = () => {
            height -= 30 / steps;
            if (height <= 0) {
                height = 0;
                clearInterval(timer);
                gameContainer.style.display = 'none';
                gameContainer.classList.remove('visible');
                gameContainer.classList.add('invisible');

            }
            gameContainer.style.height = height + '%';
        };

        const fadeOut = () => {
            opacity -= 1 / steps;
            if (opacity <= 0) {
                opacity = 0;
                clearInterval(timer);
            }
            gameContainer.style.opacity = opacity;
        };

        const timer = setInterval(() => {
            decreaseHeight();
            fadeOut();
        }, interval);
    } else {
        let opacity = 0;
        let height = 0;
        const interval = 10;
        const steps = 30;

        gameContainer.style.display = 'block';

        const increaseHeight = () => {
            height += 30 / steps;
            if (height >= 30) {
                height = 30;
                clearInterval(timer);
                gameContainer.classList.remove('invisible');
                gameContainer.classList.add('visible');
                moveLeftButton.style.display = 'block';
                moveRightButton.style.display = 'block';
                questionBox.style.display = 'block';
            }
            gameContainer.style.height = height + '%';
        };

        const fadeIn = () => {
            opacity += 1 / steps;
            if (opacity >= 1) {
                opacity = 1;
                clearInterval(timer);
            }
            gameContainer.style.opacity = opacity;
        };

        const timer = setInterval(() => {
            increaseHeight();
            fadeIn();
        }, interval);
    }
}

